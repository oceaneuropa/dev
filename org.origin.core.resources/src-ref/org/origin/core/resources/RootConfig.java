package org.origin.core.resources;

public interface RootConfig {

	/**
	 * Get Id.
	 * 
	 * @return
	 */
	public String getId();

	/**
	 * Get name.
	 * 
	 * @return
	 */
	public String getName();

	/**
	 * Get host URL.
	 * 
	 * @return
	 */
	public String getHostURL();

	/**
	 * Get context root.
	 * 
	 * @return
	 */
	public String getContextRoot();

}
